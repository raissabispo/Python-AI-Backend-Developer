dados = {"nome": "Guilherme", "idade": 28, "telefone": "9988-1781"}

#acessando os dados do dicionario
print(dados["nome"])  # "Guilherme"
print(dados["idade"])  # 28
print(dados["telefone"])  # "3333-1234"

#Alterando os dados
dados["nome"] = "Raissa"
dados["idade"] = 17
dados["telefone"] = "3333-1234"

print(dados)  # {"nome": "Raissa", "idade": 17, "telefone": "3333-1234"}