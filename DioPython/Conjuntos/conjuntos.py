# Um set é a coleção de elementos únicos, ele elimina elementos duplicados 

conjunto = set([1,2,3,3,4,5,6])
print(conjunto) # {1, 2 ,3 ,4 ,5 ,6}

letras = set("Raissa") 
print(letras)


carros = set(("palio", "gol", "celta", "palio"))
print(carros)  # {"gol", "celta", "palio"}





